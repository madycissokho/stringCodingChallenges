package JavaRevision;

public class SwitchCase {
    public static void main(String[] args) {
        int choice = 0;
        choice = 4;
        switch (choice){
            case 1:
                System.out.println("Case 1");
                break;
            case 2:
                System.out.println("Case 2");
                break;
            case 3:
                System.out.println("Case 3");
            default:
                System.out.println("Invalid Choice");
        }

    }
}
